#Variable is a word representation of a value in a program.

#Variables can be used to store data that can be used later in the program.

programming_languages = 'Python', 'Java', 'C++' #This is a tuple, which is an immutable sequence of values.
print(type(programming_languages)) #This will print the type of the variable, which is a tuple.
print(programming_languages) #This will print the tuple of programming languages.


for language in programming_languages:
    print(language)  # This will print each programming language in the tuple.
